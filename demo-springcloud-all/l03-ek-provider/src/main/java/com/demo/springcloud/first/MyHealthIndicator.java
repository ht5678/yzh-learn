@Component
public class MyHealthIndicator implements HealthIndicator {

	@Override
	public Health health() {
		if(PoliceController.canVisitDb){
			return new Health.Builder(Status.UP).build();
		}else{
			return new Health.Builder(Status.DOWN).build();
		}
	}

}
